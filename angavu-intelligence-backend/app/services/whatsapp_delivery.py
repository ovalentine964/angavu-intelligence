"""
WhatsApp Delivery Service — Report Delivery with Multi-Device Isolation

FIX 2: ALL operations scoped to specific user_id.
FIX 5: WhatsApp is OPTIONAL — only deliver to users who opted in.
FIX 11: Delivery confirmation tracking.

Architecture:
- Reports generated by backend, delivered via OpenWA
- Each delivery is scoped to a single user_id
- Delivery status tracked and confirmed
- Failed deliveries retried with exponential backoff
"""

import asyncio
import base64
import logging
import os
from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Optional

import httpx

logger = logging.getLogger(__name__)


@dataclass
class DeliveryResult:
    """Result of a report delivery attempt."""
    user_id: str
    success: bool
    message_id: Optional[str] = None
    error: Optional[str] = None
    attempts: int = 1
    delivered_at: Optional[datetime] = None
    delivery_confirmed: bool = False


@dataclass
class DeliveryStats:
    """Aggregate delivery statistics."""
    total: int = 0
    successful: int = 0
    failed: int = 0
    pending_confirmation: int = 0
    results: list = field(default_factory=list)


class WhatsAppDelivery:
    """
    Report delivery via WhatsApp.

    FIX 2: Every operation includes user_id for isolation.
    FIX 5: Only delivers to users who have connected WhatsApp.
    FIX 11: Tracks delivery confirmation.

    Usage:
        delivery = WhatsAppDelivery()
        stats = await delivery.send_daily_reports_to_all(db)
    """

    def __init__(self):
        self.openwa_url = os.environ.get("OPENWA_URL", "http://openwa:3000")
        self.max_retries = 3
        self.retry_base_delay = 2.0  # seconds

    async def send_report(
        self,
        user_id: str,
        phone: str,
        report_text: str,
        chart_base64: Optional[str] = None,
        voice_base64: Optional[str] = None,
    ) -> DeliveryResult:
        """
        Send a report to a specific worker via WhatsApp.

        FIX 2: user_id is explicitly required — no implicit context.
        FIX 11: Returns delivery confirmation status.

        Args:
            user_id: Target worker's user ID (REQUIRED for isolation)
            phone: Decrypted phone number
            report_text: Report content in Swahili
            chart_base64: Optional chart image (base64 PNG)
            voice_base64: Optional voice report (base64 OGG)

        Returns: DeliveryResult with success status and tracking info
        """
        if not user_id:
            raise ValueError("user_id is required for delivery isolation")

        if not phone:
            return DeliveryResult(
                user_id=user_id,
                success=False,
                error="No phone number available",
            )

        last_error = None

        for attempt in range(1, self.max_retries + 1):
            try:
                # Send text report first
                text_result = await self._send_text(user_id, phone, report_text)

                if not text_result.get("success"):
                    raise Exception(text_result.get("error", "Text send failed"))

                message_id = text_result.get("message_id")

                # Send chart image if provided
                if chart_base64:
                    try:
                        await self._send_image(user_id, phone, chart_base64, "Chati ya ripoti")
                    except Exception as e:
                        logger.warning(f"Chart delivery failed for {user_id}: {e}")
                        # Don't fail the whole delivery for chart failure

                # Send voice report if provided
                if voice_base64:
                    try:
                        await self._send_voice(user_id, phone, voice_base64)
                    except Exception as e:
                        logger.warning(f"Voice delivery failed for {user_id}: {e}")

                return DeliveryResult(
                    user_id=user_id,
                    success=True,
                    message_id=message_id,
                    attempts=attempt,
                    delivered_at=datetime.now(timezone.utc),
                    delivery_confirmed=False,  # Confirmed async via receipt
                )

            except Exception as e:
                last_error = str(e)
                logger.warning(
                    f"Delivery attempt {attempt}/{self.max_retries} failed for user {user_id}: {e}"
                )

                if attempt < self.max_retries:
                    # Exponential backoff
                    delay = self.retry_base_delay * (2 ** (attempt - 1))
                    await asyncio.sleep(delay)

        return DeliveryResult(
            user_id=user_id,
            success=False,
            error=last_error,
            attempts=self.max_retries,
        )

    async def send_daily_reports_to_all(self, db) -> DeliveryStats:
        """
        Send daily reports to all workers with WhatsApp connected.

        FIX 2: Each report delivery is scoped to individual user_id.
        FIX 5: Only sends to users who opted into WhatsApp.
        """
        from app.models.user import User

        # FIX 2: Query explicitly filters for WhatsApp-connected users
        users = db.query(User).filter(
            User.whatsapp_connected == True,  # noqa: E712
            User.is_active == True,  # noqa: E712
        ).all()

        logger.info(f"Sending daily reports to {len(users)} WhatsApp users")

        stats = DeliveryStats(total=len(users))

        for user in users:
            try:
                # FIX 2: Each delivery scoped to specific user
                phone = user.get_phone()
                report_text = self._generate_daily_report_text(user)

                result = await self.send_report(
                    user_id=str(user.id),
                    phone=phone,
                    report_text=report_text,
                )

                if result.success:
                    stats.successful += 1
                else:
                    stats.failed += 1

                stats.results.append(result)

            except Exception as e:
                logger.error(f"Failed to send report to user {user.id}: {e}")
                stats.failed += 1
                stats.results.append(DeliveryResult(
                    user_id=str(user.id),
                    success=False,
                    error=str(e),
                ))

        logger.info(
            f"Daily report delivery complete: {stats.successful} sent, "
            f"{stats.failed} failed out of {stats.total}"
        )

        return stats

