    # ── OpenWA Communication ────────────────────────────────────

    async def _send_text(self, user_id: str, phone: str, message: str) -> dict:
        """Send text message via OpenWA."""
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.post(
                f"{self.openwa_url}/send-message",
                json={
                    "user_id": user_id,
                    "phone": phone,
                    "message": message,
                },
            )
            response.raise_for_status()
            return response.json()

    async def _send_image(
        self, user_id: str, phone: str, image_base64: str, caption: str = ""
    ) -> dict:
        """Send image via OpenWA."""
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.post(
                f"{self.openwa_url}/send-image",
                json={
                    "user_id": user_id,
                    "phone": phone,
                    "image_url": f"data:image/png;base64,{image_base64}",
                    "caption": caption,
                },
            )
            response.raise_for_status()
            return response.json()

    async def _send_voice(self, user_id: str, phone: str, audio_base64: str) -> dict:
        """Send voice note via OpenWA."""
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.post(
                f"{self.openwa_url}/send-voice",
                json={
                    "user_id": user_id,
                    "phone": phone,
                    "audio_url": f"data:audio/ogg;base64,{audio_base64}",
                },
            )
            response.raise_for_status()
            return response.json()

