# ── Singleton ────────────────────────────────────────────────────

_bot: Optional[WhatsAppBot] = None


def get_whatsapp_bot() -> WhatsAppBot:
    """Get or create the singleton WhatsAppBot."""
    global _bot
    if _bot is None:
        _bot = WhatsAppBot()
    return _bot
